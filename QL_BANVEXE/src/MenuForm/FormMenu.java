    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        StringBuilder ab = new StringBuilder();
        if (txtMaNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ma nhan vien can nhap de tim kiem ! ");
            return;
        }
        if(ab.length()>0)
        {
            JOptionPane.showMessageDialog(this,ab);
        }

        try {
            EmployeeDao dao = new EmployeeDao();
            Employee tk = dao.findId(txtMaNV.getText());
            if (tk != null) {
                txtName.setText(tk.getHoTen());
                txtTenTK.setText(tk.getTenDN());
                txtMK.setText(tk.getMatKhau());
                txtEmail.setText(tk.getEmail());
                txtSDT.setText(tk.getSoDT());
                txtDiaChi.setText(tk.getDiaChi());
                jbtNam.setSelected(tk.getGioiTinh()==1);
                jbtNu.setSelected(tk.getGioiTinh()==0);
            }
            else {
                JOptionPane.showMessageDialog(this," Không tìm thấy tài  khoản !");
            }
        } catch (Exception ex) {
        
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

